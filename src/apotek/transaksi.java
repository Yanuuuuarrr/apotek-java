    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //menambahkan tanggal dan waktu
        LocalDateTime sekarang = LocalDateTime.now();
        //memformat tanggal menghilangkan jam,detik,milisec
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        String sekarangNew= sekarang.format(formatter);
        MessageFormat header = new MessageFormat("Data Transaksi");
        MessageFormat footer = new MessageFormat("Data dicetak pada "+sekarangNew);
        try {
            PrintRequestAttributeSet set = new HashPrintRequestAttributeSet();
            set.add(OrientationRequested.PORTRAIT);
            jTable1.print(JTable.PrintMode.FIT_WIDTH,header,footer,true,set,true);
            JOptionPane.showMessageDialog(null, "data berhasil di print");
        } catch (java.awt.print.PrinterException ex) {
            JOptionPane.showMessageDialog(null, "data tidak berhasil di print");
        }   
    }//GEN-LAST:event_jButton4ActionPerformed

